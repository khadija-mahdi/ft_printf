#include"ft_printf.h"

int	ft_hex(unsigned long n)
{
	char	*p;
	int		nb;

	nb = 0;
	p = "0123456789abcdef";
	if (n >= 0 && n < 16)
	{
		ft_putchar(p[n]);
		nb++;
	}
	else if (n >= 16)
	{
		nb += ft_hex (n / 16);
		nb += ft_hex (n % 16);
	}
	return (nb);
}
